using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class SwitchButtonScripts : MonoBehaviour
{
    [SerializeField]
    int num;
    [SerializeField]
    float offTime = 5;
    
    private Image switchImage;
    private bool isOn = false;

    private void Awake()
    {
        switchImage = GetComponent<Image>();
        //SwitchReset();
    }

    void SwitchReset()
    {
        CancelInvoke("No");
        isOn = false;
        switchImage.sprite = SwitchOffGame.Instance().offImage;
    }

    // 스위치 켜기
    public void SwitchOn()
    {
        isOn = true;
        switchImage.sprite = SwitchOffGame.Instance().onImage;
        Invoke("No", offTime);
    }

    // 스위치 끄기
    void SwitchOff()
    {
        CancelInvoke("No");
        isOn = false;
        switchImage.sprite = SwitchOffGame.Instance().offImage;
        SwitchOffGame.Instance().OffButton(num);
    }

    // 버튼 클릭 시 판단
    public void ClickMe()
    {
        TouchEffect();
        if(isOn)
        {
            Correct();
        }
        else
        {
            Fail();
        }
        SwitchOff();
    }

    // 성공
    void Correct()
    {
        SwitchOffGame.Instance().ScoreUp(20);
        SwitchOffGame.Instance().TimeDown(-5);
    }
    // 실패
    void Fail()
    {
            SwitchOffGame.Instance().TimeDown(30);
    }
    // 클릭 이펙트
    void TouchEffect()
    {

    }
    
    // 놓쳤을 때
    void No()
    {
        if (!isOn) return;
        Fail();
        SwitchOff();
    }

}
